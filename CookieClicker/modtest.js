				if (variable == 'autoClickerEnabled') {
					clearInterval(XVs_Mods.modSettings.autoClickerIntervalId);
					XVs_Mods.settings.autoClickerIntervalId = 0;
					XVs_Mods.notificationDisplayer("Auto Clicker",variable,28,10);
				}
				
